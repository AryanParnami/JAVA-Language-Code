public class Ch_01_02_litrals {
    // Literals is nothing but the right side value or a constant value
    // which we are assigning which we are declaring

    public static void main(String args[]){
        byte age = 34;
        int age2 = 56;
        short age3 = 334;
        long ageDino = 38459374673L;
        char ch = 'A';
        float f1 = 5.6f; // we use f with value other wise it will become double by defualt
        double d1 = 4.66;
        boolean b1 = true;
        String str = "Hello I am Aryan Parnami";
        System.out.println(str);
        System.out.println(age);
        System.out.println(age2);
        System.out.println(age3);
        System.out.println(ageDino);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(ch);
        System.out.println(b1);
    }
}
